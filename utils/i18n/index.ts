// 根据系统语言获取默认语言
const getDefaultLanguage = (): string => {
  const locales = Localization.getLocales();
  if (locales.length > 0) {
    const languageCode = locales[0].languageCode;
    // 如果系统语言以 "zh" 开头（中文），返回 "cn"
    if (languageCode && languageCode.startsWith("zh")) {
      return "cn";
    }
  }
  // 否则返回英文
  return "en";
};

